# IF Condition
a = 20

if a < 30:
    print("a is less than 50")

# IF / Else Condition
b = 30

if b < 20:
    print('b is less than 20')

else:
    print('b is greater than 20')

# IF / ELIF / Else Condition
c = 40

if c > 40:
    print(" c is less than 40 ")
elif c == 40:
    print("c is equal to 40")
else:
    print("c is greater than 40")
